package main

import (
	"bytes"
	"fmt"
	"log"
	"os"
	"path/filepath"

	"gopkg.in/yaml.v3"
)

// PluginManifest is the top-level structure in plugins.yaml
type PluginManifest struct {
	Plugins []struct {
		Module string `yaml:"module"`
	} `yaml:"plugins"`
}

func main() {
	log.Println("🔧 Starting plugin builder...")

	// 1. Read and parse plugins.yaml
	data, err := os.ReadFile("../plugins.yaml")
	if err != nil {
		log.Fatalf("failed to read plugins.yaml: %v", err)
	}
	var manifest PluginManifest
	if err := yaml.Unmarshal(data, &manifest); err != nil {
		log.Fatalf("failed to parse plugins.yaml: %v", err)
	}
	if len(manifest.Plugins) == 0 {
		log.Println("⚠️ No plugins specified in plugins.yaml")
		return
	}

	// 2. Generate plugins_gen.go
	genPath := filepath.Join("internal", "plugins", "plugins_gen.go")
	if err := os.MkdirAll(filepath.Dir(genPath), 0755); err != nil {
		log.Fatalf("failed to create directory: %v", err)
	}
	var buf bytes.Buffer
	buf.WriteString("// Code generated by plugin-builder; DO NOT EDIT.\n")
	buf.WriteString("package plugins\n\n")
	buf.WriteString("import (\n")
	for _, p := range manifest.Plugins {
		buf.WriteString(fmt.Sprintf("\t_ \"%s\"\n", p.Module))
	}
	buf.WriteString(")\n")
	if err := os.WriteFile(genPath, buf.Bytes(), 0644); err != nil {
		log.Fatalf("failed to write %s: %v", genPath, err)
	}
	log.Printf("✅ Generated %s\n", genPath)

	log.Println("🎉 Plugin build completed.")
}
